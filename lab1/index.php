<!-- Задание 3. Арифметические операции. Дано: $a = 27; $b = 23; Найти: Значение другого острого угла (в градусах) и катета. -->
<?php
$a = 27;
$b = 23;

$angle_rad = asin($b / $a);
$angle_deg = rad2deg($angle_rad);
$other_angle = 90 - $angle_deg;
$cathetus = sqrt($a ** 2 - $b ** 2);

echo "Острый угол: " . round($other_angle, 2) . " градусов\n";
echo "Второй катет: " . round($cathetus, 2) . "\n";
?>

<!-- Задание 9. Динамическая типизация. Дано: $a = 2; $b = 2.0; $c = ‘2’; $d = ‘two’; $g = true; $f = false; Найти: Все возможные арифметические операции над переменными, результатом которых будет целое число.-->
<?php
$a = 2;
$b = 2.0;
$c = '2';
$d = 'two';
$g = true;
$f = false;

$vars = [$a, $b, $c, $g, $f];
foreach ($vars as $x) {
    foreach ($vars as $y) {
        if (is_int($x + $y)) echo "$x + $y = " . ($x + $y) . "\n";
        if (is_int($x - $y)) echo "$x - $y = " . ($x - $y) . "\n";
        if (is_int($x * $y)) echo "$x * $y = " . ($x * $y) . "\n";
        if ($y != 0 && is_int($x / $y)) echo "$x / $y = " . ($x / $y) . "\n";
    }
}
?>

