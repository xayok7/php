<!-- Задание 50. Циклы. Найти: Посчитать сумму первых 20 четных членов натурального ряда. Выполнить в цикле с постусловием. -->
<?php
$sum = 0;
$count = 0;
$number = 2;

do {
    $sum += $number;
    $number += 2;
    $count++;
} while ($count < 20);

echo "Сумма первых 20 четных чисел: $sum\n";
?>
